% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

tic; 
data=get(handles.uitable1,'data');
global newRow1;
global max_length;
model=size(data);
if char(data(1,1))~='0'
    max_length=model(1,1);
else
    max_length=0;
end
%read the data from the excel
n_data=data(:,2);
%n_data=xlsread('Ra-r-finish','sheet1','B2:B50')
f_data=data(:,3);
ae_data=data(:,4);
ap_data=data(:,5);
Ra_data=data(:,6);
r_data=data(:,7);
Vw_data=data(:,8);
%Matrix transpose
n=n_data';
f=f_data';
ae=ae_data';
ap=ap_data';
Ra=Ra_data';
r=r_data';
Vw=Vw_data';
%define the initialize data:
p=[n;f;ae;ap]
v=[Ra;r;Vw]
net=newff(minmax(p),[15,3],{'tansig','purelin'},'traingdm')
% inputWeights=net.IW{1,1}
% inputbias=net.b{1}
% layerWeights=net.LW{2,1}
% layerbias=net.b{2}
net.trainParam.show=50
net.trainParam.lr=0.07
net.trainParam.mc=0.03
net.trainParam.epochs=400000
net.trainParam.goal=1e-3
net.trainParam.showWindow=0;
%train network
[net,tr]=train(net,p,v);
%output the answer about the simulation.
newRow1=handles.newRow1;
x=1:max_length;
out=sim(net,p)
p_test=newRow1';
out1=sim(net,p_test)
set(handles.text32,'string',num2str(out1(1,1)));
set(handles.text33,'string',num2str(out1(2,1)));
set(handles.text34,'string',num2str(out1(3,1)));

%show the error value
ra_error=abs((out1(1,1)-Ra_data(max_length,1))/Ra_data(max_length,1));
set(handles.text41,'string',num2str(ra_error*100));
r_error=abs((out1(1,1)-r_data(max_length,1))/r_data(max_length,1));
set(handles.text42,'string',num2str(r_error*100));
Vw_error=abs((out1(1,1)-Vw_data(max_length,1))/Vw_data(max_length,1));
set(handles.text43,'string',num2str(Vw_error*100));
%max_length=get(handles.pushbutton3,'userdata');
plot(handles.axes1,x,out,'*')
hold on
plot(handles.axes1,x,Ra,'o')
save neu8 net;
toc


